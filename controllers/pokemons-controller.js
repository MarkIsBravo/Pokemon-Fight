const Pokemon=require('../models/pokemon');
const User=require('../models/user');
const pokemonsController={};

pokemonsController.index=(req,res)=>{
    User.findUserPokemons(req.user.id)
    .then(pokemon=>{
        res.render('pokemons/pokemon-index',{
            currentPage:'index',
            message:'ok',
            user:req.user,
            data:pokemon
        });
    })
    .catch(err=>{
        console.log(err);
        res.status(500).render('error',{
            currentPage:'whoops',
        });
    })
};

pokemonsController.create=(req,res)=>{
    Pokemon.create({
        name:req.body.name,
        type:req.body.type,
        attack:req.body.attack,
        defense:req.body.defense
    },req.user.id)
    .then(pokemon=>{
        res.send(pokemon)
    })
    .catch(err=>{
        console.log(err);
        res.status(500).render('error',{
            currentPage:'whoops',
        });
    });
    User.addPokemon(req.user.id)
    .catch(err=>{
        console.log(err);
        res.status(500).render('error',{
            currentPage:'whoops',
        });
    })
};

pokemonsController.show=(req,res)=>{
    Pokemon.findById(req.params.id)
    .then(pokemon=>{
        res.render('pokemons/pokemon-single',{
            currentPage:'show',
            message:'ok',
            data:pokemon
        })
    })
    .catch(err=>{
        console.log(err);
        res.status(500).render('error',{
            currentPage:'whoops',
        });
    });
};

pokemonsController.showOther=(req,res)=>{
    Pokemon.findById(req.params.id)
    .then(pokemon=>{
        res.render('pokemons/pokemon-singleOther',{
            currentPage:'showOther',
            message:'ok',
            data:pokemon
        })
    })
    .catch(err=>{
        console.log(err);
        res.status(500).render('error',{
            currentPage:'whoops',
        });
    });
};

pokemonsController.delete=(req,res)=>{
    Pokemon.destroy(req.params.id)
    .then(()=>{
        res.redirect('/pokemons/pokemon-index');
    }).catch(err=>{
        console.log(err);
        res.status(500).render('error',{
            currentPage:'whoops',
        });
    });
    User.deletePokemon(req.user.id)
    .catch(err=>{
        console.log(err);
        res.status(500).render('error',{
            currentPage:'whoops',
        });
    })
};

module.exports=pokemonsController;